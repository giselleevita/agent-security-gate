#!/usr/bin/env python3
"""Publish CI benchmark comparison as docs/benchmark-results/latest.md + latest.json."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

from benchmark.report import render_comparison_report

ROOT = Path(__file__).resolve().parents[1]
DEFAULT_COMPARISON = ROOT / "results" / "comparison.json"
DEFAULT_SUMMARY = ROOT / "results" / "summary.json"
OUT_DIR = ROOT / "docs" / "benchmark-results"
OUT_MD = OUT_DIR / "latest.md"
OUT_JSON = OUT_DIR / "latest.json"
DEFAULT_SCENARIOS = ROOT / "benchmark" / "scenarios" / "scenarios.yaml"


def _header(comparison: dict) -> str:
    gate = comparison["baselines"]["gate"]
    no_gate = comparison["baselines"]["no_gate"]
    deltas = comparison["deltas"]
    return "\n".join(
        [
            "<!-- Auto-generated by scripts/publish_benchmark_results.py -->",
            "",
            "# Latest benchmark results",
            "",
            "| Metric | No gate | Policy gate | Delta |",
            "|---|---:|---:|---:|",
            f"| ASR | {no_gate['asr']:.1%} | {gate['asr']:.1%} | {deltas['asr_reduction']:.1%} reduction |",
            f"| Leakage | {no_gate['leakage_rate']:.1%} | {gate['leakage_rate']:.1%} | {deltas['leakage_reduction']:.1%} reduction |",
            f"| False positives | {no_gate['false_positive_rate']:.1%} | {gate['false_positive_rate']:.1%} | {deltas['false_positive_change']:+.1%} |",
            f"| Benign task success | {no_gate['task_success_rate']:.1%} | {gate['task_success_rate']:.1%} | {deltas['task_success_change']:+.1%} |",
            "",
        ]
    )


def publish(comparison_path: Path, summary_path: Path | None = None) -> None:
    comparison = json.loads(comparison_path.read_text(encoding="utf-8"))
    summary = None
    if summary_path and summary_path.is_file():
        summary = json.loads(summary_path.read_text(encoding="utf-8"))
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    report = render_comparison_report(
        comparison,
        scenarios_path=DEFAULT_SCENARIOS if DEFAULT_SCENARIOS.is_file() else None,
        summary=summary,
    )
    OUT_MD.write_text(_header(comparison) + report, encoding="utf-8")
    payload = {"comparison": comparison}
    if summary_path and summary_path.is_file():
        payload["summary"] = json.loads(summary_path.read_text(encoding="utf-8"))
    OUT_JSON.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")
    print(f"Wrote {OUT_MD} and {OUT_JSON}")


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--comparison", type=Path, default=DEFAULT_COMPARISON)
    parser.add_argument("--summary", type=Path, default=DEFAULT_SUMMARY)
    args = parser.parse_args(argv)
    if not args.comparison.is_file():
        print(f"comparison file not found: {args.comparison}", file=sys.stderr)
        return 1
    publish(args.comparison, args.summary if args.summary.is_file() else None)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
